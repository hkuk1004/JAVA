package ch02;

public class AsignOp2 {
	public static void main(String[] args) {
		int i1 = 8, i2 = 4;
		i1 += i2; // i1 = i1 + i2;
		System.out.println("i1 = "+ i1 + ",i2 = " + i2);
		i1 -= i2; // i1 = i1 - i2;
		System.out.println("i1 = " + i1 + ",i2 = " + i2);
		i1 *= i2; // i1 = i1 * i2
		System.out.println("i1 = " + i1 + ",i2 = " + i2);
		i1 /= i2; // i1 = i1 / i2
		System.out.println("i1 = " + i1 + ",i2 = " + i2);
		i1 %= i2; // i1 = i1 % i2
		System.out.println("i1 = " + i1 + ",i2 = " + i2);
		
	}
}
